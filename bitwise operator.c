/*Bitwise operator = 1.&(Bitwise AND) 2.|(Bitwise OR) 3.^(Bitwise ExOR)
 4. >>(Right shift 5.<<(Left shift) 6. ~(Bitwise NOT)*/

#include<stdio.h>
int main()
{
    int a = 32;
    int b = 12;
    int c;

    c = a&b;
    printf("c = %d\n",c);

    c = a|b;
    printf("c = %d\n",c);

    c = a^b;
    printf("c = %d\n",c);

}









































































































